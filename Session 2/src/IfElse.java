public class IfElse
{
    public static void main(String[] args)

    {
        int x = 30;
        if (x>60)
            {
                System.out.println("The value is > 60");
            }else if (x>50)
            {
                System.out.println("The value is > 50");

            }else
            {
                System.out.println("The value is <= 50");
            }

    }
}


